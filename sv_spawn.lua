function pSpawn(ply)
	local decider = math.random(1,8)
	local decider2 = math.random(1,12)
	if ( string.find( string.lower( game.GetMap() ), "rp_apocalypse" ) ) then
	ply:SetPos(Vector(spawnapocalypse[decider][1], spawnapocalypse[decider][2], spawnapocalypse[decider][3]))
	end
	if ( string.find( string.lower( game.GetMap() ), "rp_pripyat_fixed" ) ) then
	ply:SetPos(Vector(spawnpripyat[decider][1], spawnpripyat[decider][2], spawnpripyat[decider][3]))
	ply:SetEyeAngles(Angle(0, 0, 0))
	end
	if ( string.find( string.lower( game.GetMap() ), "gm_fork" ) ) then
	ply:SetPos(Vector(spawnfork[decider][1], spawnfork[decider][2], spawnfork[decider][3]))
	ply:SetEyeAngles(Angle(0, 180, 0))
	end
	if ( string.find( string.lower( game.GetMap() ), "ud_lockedwaste" ) ) then
	ply:SetPos(Vector(lockedwaste[decider2][1], lockedwaste[decider2][2], lockedwaste[decider2][3]))
	ply:SetEyeAngles(Angle(0, 180, 0))
	end
	if ( string.find( string.lower( game.GetMap() ), "rp_evocity2_v2p" ) ) then
	ply:SetPos(Vector(evocity[decider2][1], evocity[decider2][2], evocity[decider2][3]))
	ply:SetEyeAngles(Angle(0, 90, 0))
	end
	if ( string.find( string.lower( game.GetMap() ), "rp_rockford_v1b" ) ) then
	ply:SetPos(Vector(rockford[decider2][1], rockford[decider2][2], rockford[decider2][3]))
	ply:SetEyeAngles(Angle(0, 90, 0))
	end
	if ( string.find( string.lower( game.GetMap() ), "rp_rockford_v2b" ) ) then
	ply:SetPos(Vector(rockford2[decider2][1], rockford2[decider2][2], rockford2[decider2][3]))
	ply:SetEyeAngles(Angle(0, 90, 0))
	end
	if ( string.find( string.lower( game.GetMap() ), "hgn_srp_chernobyl_beta" ) ) then
	ply:SetPos(Vector(chernobyl[decider2][1], chernobyl[decider2][2], chernobyl[decider2][3]))
	ply:SetEyeAngles(Angle(0, 270, 0))
	end
	if ( string.find( string.lower( game.GetMap() ), "sep11_thecordon" ) ) then
	ply:SetPos(Vector(thecordon[decider2][1], thecordon[decider2][2], thecordon[decider2][3]))
	ply:SetEyeAngles(Angle(0, 160, 0))
	end
	if ( string.find( string.lower( game.GetMap() ), "rp_salvation_2_stalker" ) ) then
	ply:SetPos(Vector(salvation[decider2][1], salvation[decider2][2], salvation[decider2][3]))
	ply:SetEyeAngles(Angle(0, 0, 0))
	end
	if ( string.find( string.lower( game.GetMap() ), "rp_lr_refuge_v1" ) ) then
	ply:SetPos(Vector(refuge[decider2][1], refuge[decider2][2], refuge[decider2][3]))
	ply:SetEyeAngles(Angle(0, 160, 0))
	end
end
